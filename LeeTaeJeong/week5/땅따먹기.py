def solution(land):
    # 1행(인덱스 0)은 그대로 두고, 2행(인덱스 1)부터 마지막 행까지 반복합니다.
    for i in range(1, len(land)):
        # 현재 행(i)의 각 열(0~3)에 대해, 
        # 이전 행(i-1)에서 '자신의 열을 제외한' 나머지 3개 중 최댓값을 더해 나갑니다.
        
        # 0번 열을 밟는 경우: 이전 행의 1, 2, 3번 열 중 최대값 더하기
        land[i][0] += max(land[i-1][1], land[i-1][2], land[i-1][3])
        
        # 1번 열을 밟는 경우: 이전 행의 0, 2, 3번 열 중 최대값 더하기
        land[i][1] += max(land[i-1][0], land[i-1][2], land[i-1][3])
        
        # 2번 열을 밟는 경우: 이전 행의 0, 1, 3번 열 중 최대값 더하기
        land[i][2] += max(land[i-1][0], land[i-1][1], land[i-1][3])
        
        # 3번 열을 밟는 경우: 이전 행의 0, 1, 2번 열 중 최대값 더하기
        land[i][3] += max(land[i-1][0], land[i-1][1], land[i-1][2])
        
    # 마지막 행까지 계산이 끝났다면, 마지막 행의 4개 값 중 가장 큰 값이 정답입니다.
    return max(land[-1])